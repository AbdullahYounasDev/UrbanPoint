const { default: mongoose } = require("mongoose");

const contactScheam = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: Number,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const Contact =
  mongoose.models.contacts || mongoose.model("contacts", contactScheam);

export default Contact;
